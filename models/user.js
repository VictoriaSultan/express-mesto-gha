const mongoose = require('mongoose');

const userSchema = {
  name: {
    type: String,
    minlength: 2,
    maxlength: 30,
    required: true,
  },
  about: {
    type: String,
    minlength: 2,
    maxlength: 30,
    required: true,
  },
  avatar: {
    type: String,
  },
};

module.exports = mongoose.model('user', userSchema);
